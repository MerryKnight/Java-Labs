
public class Employee
{
    private String name;
    private double salary;
    
    public String getName()
    {
        return name;
    }
    public double getSal()
    {
        return salary;
    }
    public void setName(String name)
    {
        this.name = name;
    }
    public void setSal(double sal)
    {
        this.salary = sal;
    }
}
